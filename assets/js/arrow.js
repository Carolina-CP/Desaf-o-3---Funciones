
// funcion original 

suma = function example(a,b){
    return a + b
}

/* para transformar una declaración de 
función en una funcion flecha se comienza 
con el nombre de la variable que contiene la función,
seguida por el simbolo de asignación, luego los 
parámentros segudo del => y el argumento que se 
desea retornar */

suma = (a,b) => a + b